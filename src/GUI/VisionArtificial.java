    private void correVideo(String path){
        //System.out.println("EEEEEEEEEEEO");
        System.loadLibrary(Core.NATIVE_LIBRARY_NAME);
        VideoCapture camara = new VideoCapture(0);  
        if (!camara.isOpened()) {
            System.out.println("Error!");
        }else{
            Mat frame = new Mat(); 
            while(true){
                if (camara.read(frame)) {
                    System.out.println("Frame obtenido");
                    System.out.println("Ancho del frame: "+frame.width()+"Alto del frame: "+frame.height());   
                    this.video.setIcon(new ImageIcon(img.toImage(img.Mat2BufferedImage(frame))));
                    this.repaint();
                }
            }
        }
    }
